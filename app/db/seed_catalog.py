async def seed():
    async with async_session() as db:
        # Проверим, есть ли уже книги в каталоге
        from sqlalchemy import select, func
        count = await db.scalar(select(func.count()).select_from(CatalogBook))
        if count and count > 0:
            print(f"Каталог уже содержит {count} книг. Пропускаем.")
            return

        for book_data in CATALOG:
            book = CatalogBook(**book_data)
            db.add(book)

        await db.commit()
        print(f"Добавлено {len(CATALOG)} книг в каталог.")


if __name__ == "__main__":
    asyncio.run(seed())